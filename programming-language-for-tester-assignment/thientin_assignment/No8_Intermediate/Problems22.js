function convertCtoF(celsius) {
    // Công thức chuyển đổi từ C sang F: (Celsius * 9/5) + 32
    return (celsius * 9/5) + 32;
  }
  
  const inputCelsius = 0;
  const result = convertCtoF(inputCelsius);
  console.log(result);

  const inputCelsius2 = 100;
  const result2 = convertCtoF(inputCelsius2);
